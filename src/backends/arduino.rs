/// Bare identifiers that name Arduino constants.
fn map_ident(name: &str) -> String {
    match name {
        "output" => "OUTPUT".into(),
        "input" => "INPUT".into(),
        "input_pullup" => "INPUT_PULLUP".into(),
        "on" => "HIGH".into(),
        "off" => "LOW".into(),
        _ => format!("v_{name}"),
    }
}

fn is_arduino_math(name: &str) -> bool {
    matches!(
        name,
        "round" | "round_up" | "round_down" | "square_root" | "power"
            | "absolute" | "random" | "random_number"
    )
}

fn binop_sym(op: &BinOp) -> &'static str {
    match op {
        BinOp::Add => "+",
        BinOp::Sub => "-",
        BinOp::Mul => "*",
        BinOp::Div => "/",
        BinOp::Mod => "%",
        BinOp::Eq => "==",
        BinOp::NotEq => "!=",
        BinOp::Lt => "<",
        BinOp::Lte => "<=",
        BinOp::Gt => ">",
        BinOp::Gte => ">=",
        BinOp::And => "&&",
        BinOp::Or => "||",
    }
}

fn escape_cpp(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            _ => out.push(c),
        }
    }
    out
}

// ----------------------------------------------------------------
// Local hoisting (wow variables are function-scoped, C++ blocks are not)
// ----------------------------------------------------------------

fn declare_locals(
    body: &[Spanned<Node>],
    params: &[String],
    globals: &BTreeSet<String>,
    depth: usize,
) -> String {
    let mut names = BTreeSet::new();
    for stmt in body {
        collect_assigns(stmt, &mut names);
    }
    let pad = "    ".repeat(depth);
    let mut out = String::new();
    for name in &names {
        if params.iter().any(|p| p == name) || globals.contains(name) {
            continue;
        }
        out.push_str(&format!("{pad}double v_{name} = 0;\n"));
    }
    out
}

fn collect_assigns(node: &Spanned<Node>, names: &mut BTreeSet<String>) {
    match &node.node {
        Node::Assign { name, .. } => {
            names.insert(name.clone());
        }
        Node::Agar { then_body, else_ifs, else_body, .. } => {
            for s in then_body {
                collect_assigns(s, names);
            }
            for (_, body) in else_ifs {
                for s in body {
                    collect_assigns(s, names);
                }
            }
            if let Some(body) = else_body {
                for s in body {
                    collect_assigns(s, names);
                }
            }
        }
        Node::HarRange { body, .. } | Node::Baar { body, .. } | Node::Jabtak { body, .. } => {
            for s in body {
                collect_assigns(s, names);
            }
        }
        _ => {}
    }
}

fn uses_bol_in(node: &Spanned<Node>) -> bool {
    match &node.node {
        Node::Bol(_) => true,
        Node::ArduinoShuru(body) | Node::ArduinoChalao(body) | Node::Kaam { body, .. } => {
            body.iter().any(uses_bol_in)
        }
        Node::Agar { then_body, else_ifs, else_body, .. } => {
            then_body.iter().any(uses_bol_in)
                || else_ifs.iter().any(|(_, b)| b.iter().any(uses_bol_in))
                || else_body.as_ref().map_or(false, |b| b.iter().any(uses_bol_in))
        }
        Node::HarRange { body, .. } | Node::Baar { body, .. } | Node::Jabtak { body, .. } => {
            body.iter().any(uses_bol_in)
        }
        _ => false,
    }
}
