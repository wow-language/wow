// Variable hoisting
// ----------------------------------------------------------------

/// Emit `WowValue v_x = wow_null();` for every variable assigned anywhere in a
/// body. wow variables are function-scoped (like JS `var`), but C blocks are
/// not, so we hoist every assignment to the top of the enclosing function.
fn declare_locals(body: &[Spanned<Node>], params: &[String], depth: usize) -> String {
    let mut names = BTreeSet::new();
    for stmt in body {
        collect_assigns(stmt, &mut names);
    }
    let pad = "    ".repeat(depth);
    let mut out = String::new();
    for name in &names {
        if params.iter().any(|p| p == name) {
            continue;
        }
        out.push_str(&format!("{pad}WowValue v_{name} = wow_null();\n"));
    }
    out
}

fn collect_assigns(node: &Spanned<Node>, names: &mut BTreeSet<String>) {
    match &node.node {
        Node::Assign { name, .. } => {
            names.insert(name.clone());
        }
        Node::Agar { then_body, else_ifs, else_body, .. } => {
            for s in then_body {
                collect_assigns(s, names);
            }
            for (_, body) in else_ifs {
                for s in body {
                    collect_assigns(s, names);
                }
            }
            if let Some(body) = else_body {
                for s in body {
                    collect_assigns(s, names);
                }
            }
        }
        Node::HarRange { body, .. }
        | Node::HarList { body, .. }
        | Node::Baar { body, .. }
        | Node::Jabtak { body, .. } => {
            for s in body {
                collect_assigns(s, names);
            }
        }
        Node::Koshish { body, catch_body, .. } => {
            for s in body {
                collect_assigns(s, names);
            }
            for s in catch_body {
                collect_assigns(s, names);
            }
        }
        _ => {}
    }
}

/// Does this subtree contain a koshish? Gates the handler-stack bookkeeping.
fn uses_koshish(node: &Spanned<Node>) -> bool {
    match &node.node {
        Node::Koshish { .. } => true,
        Node::Banao { body, .. }
        | Node::HarRange { body, .. }
        | Node::HarList { body, .. }
        | Node::Baar { body, .. }
        | Node::Jabtak { body, .. } => body.iter().any(uses_koshish),
        Node::Agar { then_body, else_ifs, else_body, .. } => {
            then_body.iter().any(uses_koshish)
                || else_ifs.iter().any(|(_, b)| b.iter().any(uses_koshish))
                || else_body.as_ref().map_or(false, |b| b.iter().any(uses_koshish))
        }
        _ => false,
    }
}

