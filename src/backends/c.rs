fn prototype(name: &str, params: &[Param]) -> String {
    if params.is_empty() {
        format!("WowValue f_{name}(void);\n")
    } else {
        let ps: Vec<&str> = params.iter().map(|_| "WowValue").collect();
        format!("WowValue f_{name}({});\n", ps.join(", "))
    }
}

fn binop_fn(op: &BinOp) -> &'static str {
    match op {
        BinOp::Add => "wow_add",
        BinOp::Sub => "wow_sub",
        BinOp::Mul => "wow_mul",
        BinOp::Div => "wow_div",
        BinOp::Mod => "wow_mod",
        BinOp::Eq => "wow_eq",
        BinOp::NotEq => "wow_neq",
        BinOp::Lt => "wow_lt",
        BinOp::Lte => "wow_lte",
        BinOp::Gt => "wow_gt",
        BinOp::Gte => "wow_gte",
        BinOp::And => "wow_and",
        BinOp::Or => "wow_or",
    }
}

/// tools whose argument is an implicit-`x` predicate.
fn is_higher_order(name: &str) -> bool {
    matches!(name, "badlo" | "chuno" | "dhundo" | "guroh")
}

/// tools and shared keywords implemented in the C runtime.
fn is_builtin(name: &str) -> bool {
    matches!(
        name,
        // math
        "round" | "round_up" | "round_down" | "square_root" | "power"
            | "absolute" | "random" | "random_number"
        // strings
            | "lambai" | "bara_likho" | "chota_likho" | "saaf" | "toro" | "milao" | "tabdeel"
        // collections
            | "ginti" | "jama" | "max" | "min" | "pehla" | "aakhri" | "ulta"
            | "shamil" | "alag" | "silsila" | "tukre" | "flatten" | "tarteeb"
            | "phento"
        // higher-order (also matched earlier)
            | "badlo" | "chuno" | "dhundo" | "guroh" | "joro"
        // shared
            | "intezar"
        // objects
            | "mafta" | "qeemtain" | "key_hai" | "hata"
    )
}

fn escape_c(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            _ => out.push(c),
        }
    }
    out
}
